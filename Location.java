public class Location {
    private double x; // x-coordinate of location

    private double y; // y-coordinate of location

    /**
     * Getter method of the x-coordinate.
     * @return
     */
    public double getX() {
        return x;
    }

    /**
     * Getter method of the y-coordinate
     * @return
     */
    public double getY() {
        return y;
    }

    /**
     * Setter method of the x-coordinate.
     * @param x
     */
    public void setX(double x){
        this.x = x;
    }

    /**
     * Setter method of the y-coordinate.
     * @param y
     */
    public void setY(double y){
        this.y = y;
    }
}
